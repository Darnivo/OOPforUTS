
public class Bycicle {
	protected int gear, speed;
	private int brake;


	public Bycicle(int gear, int speed) {
		this.gear = gear;
		this.speed = speed;
	}
	
	
	public int getGear() {
		return gear;
	}

	public void setGear(int gear) {
		this.gear = gear;
	}

	public int getSpeed() {
		return speed;
	}

	public void setSpeed(int speed) {
		this.speed = speed;
	}

	public int getBrake() {
		return brake;
	}


	public void setBrake(int brake) {
		this.brake = brake;
	}

	

	
}
